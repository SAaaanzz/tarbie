// Lesson-plan generation — system prompt, JSON schema and few-shot examples
// used by the `lesson_plan` assistant category (Claude API, structured output).

export type LessonPlanLang = 'kz' | 'ru';

export interface LessonPlanStage {
  order: number;
  name: string;
  minutes: number;
  content: string;
}

export interface LessonPlanGroupWork {
  group: string;
  title: string;
  situation: string;
  questions: string[];
  conclusion: string;
}

export interface LessonPlan {
  topic_title: string;
  goals: string[];
  resources: string[];
  stages: LessonPlanStage[];
  relevance: string;
  group_work: LessonPlanGroupWork[];
  reflection: { method: string; tool_url?: string; question: string };
}

